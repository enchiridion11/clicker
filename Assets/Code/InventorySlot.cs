using System;
using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class InventorySlot : MonoBehaviour {
    #region Fields

    #endregion

    #region Properties

    public InventoryItemPresenter ItemPresenter { get; set; }

    public string ItemId { get; set; }

    public int Amount { get; set; }

    public bool IsEmpty { get; set; }

    #endregion

    #region Events

    #endregion

    #region Methods

    #region Unity

    void OnDisable () {
        UnsubscribeFromEvents ();
    }

    #endregion

    public void Initialize () {
        SubscribeToEvents ();

        IsEmpty = true;
    }

    void SubscribeToEvents () {
        InventoryManager.Instance.OnAddItem += IncreaseAmount;
    }

    void UnsubscribeFromEvents () {
        InventoryManager.Instance.OnAddItem -= IncreaseAmount;
    }

    void IncreaseAmount (string itemId, int currentAmount) {
        if (ItemId == itemId) {
            Amount++;
            ItemPresenter.SetAmount (Amount);
        }
    }

    public void DecreaseAmount (int amount) {
        Amount -= amount;
        if (Amount > 0) {
            ItemPresenter.SetAmount (Amount);
        }
        else {
            RemoveItem ();
        }
    }

    public void RemoveItem () {
        Destroy (ItemPresenter.gameObject);
        ItemPresenter = null;
        ItemId = null;
        Amount = 0;
        IsEmpty = true;
    }

    #endregion
}